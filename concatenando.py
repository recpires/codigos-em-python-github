# Concatenando duas strings de entrada do usuário
string1 = input("Digite a primeira string: ")
string2 = input("Digite a segunda string: ")
result_string = string1 + " " + string2
print("Resultado da concatenação das strings:", result_string)

# Concatenando duas listas de entrada do usuário
list1 = input("Digite a primeira lista de números separados por vírgula: ").split(',')
list2 = input("Digite a segunda lista de números separados por vírgula: ").split(',')
result_list = list1 + list2
print("Resultado da concatenação das listas:", result_list)